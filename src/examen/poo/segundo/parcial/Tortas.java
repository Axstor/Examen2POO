    int c, edad, precio,total;
     float totp;
     float a;
        String nombre = txtNom.getText() ;
        c = Integer.parseInt(txtCantidad.getText());
        edad = Integer.parseInt(txtEdad.getText()); 
        a =(float) .05;
        precio = (42);
        total=(precio*c);
            totp= (total*a);
            float pre=(total-totp);//en este caso se restara el 5% porque es descuento
        if(c>3 && c>3){//si la cantidad es mayor o menor a 3 se realiza lo siguiente
              txtArea.append("Nombre del cliente: "+nombre+"\nEdad del cliente: "+edad+"\nLa cantidad que ordeno es de: "+c+"\n");
            txtArea.append("Son "+c+" pedidos por lo que debera de pagar "+total+ " \n");
        }else{
        if(c==3){//si la cantidad pedida es igual 3 se realizara lo siguiente
              txtArea.append("Nombre del cliente: "+nombre+"\nEdad del cliente: "+edad+"\nLa cantidad que ordeno es de: "+c+"\n");
             txtArea.append("Tendra un descuento del 5% "+pre+ "\n");
        }
        }
        
